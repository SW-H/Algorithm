#include <cstdio>
 int main(){
    int T,n,res;
    long long s[100001][4]={0};
    scanf("%d", &T);
    s[1][1]=s[2][2]=s[3][1]=s[3][2]=s[3][3]=1;
    s[1][2]=s[1][3]=s[2][1]=s[2][3]=0;
    for(int i=4;i<=100000;i++){
        s[i][1]=(s[i-1][2]+s[i-1][3])%1000000009;
        s[i][2]=(s[i-2][1]+s[i-2][3])%1000000009;
        s[i][3]=(s[i-3][1]+s[i-3][2])%1000000009;
    }
 
    for(int i=0;i<T;i++){
        scanf("%d", &n);
        res = (s[n][1]+s[n][2]+s[n][3])%1000000009;
        printf("%d\n",res);
    }
 }
